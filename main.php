<p class="odcText">OpenDataCincy is a portal that provides access to various data sets, applications, and APIs related to the Greater Cincinnati region. OpenDataCincy is based on the idea that providing free and easy access to data information encourages better and more transparent government and a more engaged and knowledgeable citizenry. By connecting people with data, OpenDataCincy is hoping to encourage users to take the data and transform it into creative applications, projects, and visualizations that demonstrate the power that data can have in understanding and shaping our communities.<br><br>
This website was created with Data from OpenDataCincy, primarily the Non-Emergency Service Requests by year and the Vacant Property Records, by Ali Jaouhari and Kristina Wegener for Northern Kentucky University's Software Engineering class, Fall 2014. All data is assumed accurate as given.</p>
